# list is a structure to store lists of information in python
# allows us to organize them and keep track of them
# store multiple values inside same variable
friends = ["Josh", "Jon", "Craig", "Michael"]
# print index value of friends list, 0 is starting value
print(friends[0])
print(friends[1])
print(friends[2])
# print index negative value equals last position from left to right
print(friends[-1])
# print from index starting at 1
print(friends[1:])
print(friends[2:4])
# change index value of 1
friends[1] = "Toby"
print(friends[1:3])