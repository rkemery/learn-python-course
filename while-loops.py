# while loops is a structure in python
# executes a block of code multiple times
# loops through code until condition is false

# create integer
i = 1
# while condition / keep looping inside as long as condition is true
# when condition turns false the loop stops
while i <= 10:
    # while loop
    print(i)
    # increment loop
    # shorthand to increment 1 in loop
    i += 1
# else if statement is false then say loop is done
else:
    print("Done with loop")
